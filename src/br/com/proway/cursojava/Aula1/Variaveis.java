package br.com.proway.cursojava.Aula1;

public class Variaveis {

    public static void main(String[] args) {
        //Declaração de variável segue o seguinte modelo: tipo + identificador podendo receber o valor.
        String nome = "João da Silva";
        //No exemplo acima String é o tipo, nome é o identificador e a atribuição é ="João da silva"
        int idade = 18;
        boolean valido = true;
        double preco = 19.97;

        System.out.println("O nome é "+nome);
        System.out.println("A idade é "+idade);
        System.out.println("É valido? "+valido);
        System.out.println("O preço é "+preco);




    }
}
